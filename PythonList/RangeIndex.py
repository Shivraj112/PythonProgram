animals = ["cat","dog","bat","mouse","pig","horse","donkey","goat","cow"]
print(animals[3:7])
print(animals[-7:-2])

# Printing all the elements

print(animals[:6])
print(animals[:-3])

# Printing the alternate values

print(animals[::2])
print(animals[-8:-1:2])

# Priting every 3rd consecutive values within a given range

print(animals[1:8:3])